export default{
    vuex: {
        getters: {
            getProductData: msgProductDataGetter
        }
    },
    created: function () {
        if (!this.getProductData.curProductData.inMyField) {
            var sign = sessionStorage.getItem('originalUrlSign');
            var usage = sessionStorage.getItem('usage');
            sign = JSON.parse(sign);
            this.$router.go({
                name: 'productDetail',
