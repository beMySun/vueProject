            });
        };
    },
    ready: function () {
        //处理要传到条款页的数据
        var productName = this.getProductData.productData.productName;
        var clauseName = '',
            planCode = '';
        if (this.getProductData.curProductData.inMyField) {
            for (var i = 0; i < this.getProductData.productData.clauses.length; i++){
                clauseName += this.getProductData.productData.clauses[i].clauseName + '/';
                planCode += this.getProductData.productData.clauses[i].planCode + '/';
            }
            //添加跳转链接
